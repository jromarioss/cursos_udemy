/* 
  valor igualdade ==
  valor igualdade e tipo ===
  >, <, >=, <=, !=, !==
*/

let n1 = 10
let n2 = '10'

console.log(n1 == n2) // true
console.log(n1 === n2) // false
console.log(n1 > n2) // false
console.log(n1 < n2) // false
console.log(n1 >= n2) // true
console.log(n1 <= n2) // true
console.log(n1 != n2) // false
console.log(n1 !== n2) // true